{-# LANGUAGE FlexibleContexts #-}
{-# LANGUAGE OverloadedStrings #-}
{-# LANGUAGE Safe #-}
-- | The Cryptol backend: a direct translation from Core to a self-contained
--   Cryptol module, intended for equivalence checking against hand-written
--   Cryptol specs (e.g. with SAW) and for fast functional simulation. Every
--   Core defn becomes a pure function over bitvectors, and the whole device
--   becomes a stream function @rw_device@ mapping a sequence of per-cycle
--   inputs to a sequence of per-cycle outputs, matching the semantics of the
--   Core interpreter (ReWire.Core.Interp) bit for bit -- including the
--   don't-care convention that a call with a nil alternative takes its branch
--   unconditionally. The rwc-test cosimulation check compares a cryptol
--   evaluation of @rw_device@ against the interpreter's trace.
--
--   Primitive operations mirror the interpreter's semantics: arithmetic is
--   performed at the width @max(result, operands)@ (so carries are preserved,
--   as with Verilog assignment context widths), while complement, shifts, and
--   XNOR are performed at the (left) operand's own width and then resized.
--
--   Externs with no clock and reset are declared as uninterpreted functions
--   in a @parameter@ block (their generic parameters become leading 32-bit
--   arguments); clocked externs and references (rwPrimSetRef/rwPrimGetRef)
--   have no pure-function equivalent and are rejected.
module ReWire.Core.ToCryptol (compileProgram) where

import ReWire.Annotation (Annote)
import ReWire.BitVector (BV, width, bitVec, nat, zeros, (==.))
import ReWire.Config (Config)
import ReWire.Core.Interp (patApply', patMatches', subRange, dispatchWires, resumptionSize, Wiring')
import ReWire.Core.Syntax as C hiding (Name, Size)
import ReWire.Cryptol.Syntax (cryptolName)
import ReWire.Error (failAt, AstError, MonadError)
import ReWire.Pretty (showt)

import qualified ReWire.Cryptol.Syntax as Cry

import Control.Arrow ((&&&))
import Control.Monad (unless)
import Control.Monad.State (StateT, runStateT, modify, gets)
import Data.HashMap.Strict (HashMap)
